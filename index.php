      const name = document.getElementById("name");
      const email = document.getElementById("email");
      const number = document.getElementById("number");

      function validateFirstName() {
        const check = /^[a-zA-Z]{2,}$/;
        if (!check.test(name.value)) {
          alert(
            "First name should contain alphabets and the length should not be less than 2 characters"
          );
          return false;
        }
        return true;
      }


      function validateEmail() {
        const check = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!check.test(email.value)) {
          alert("Invalid email address format");
          return false;
        }
        return true;
      }

      function validatePhone() {
        const regex = /^\d{10}$/;
        if (!regex.test(number.value)) {
          alert("Phone number should contain 10 digits only");
          return false;
        }
        return true;
      }


      function validateForm() {
        return (
          validateFirstName() && validateEmail() && validatePhone()
        );
      }
